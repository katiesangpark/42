#include <unistd.h>

int main(int argc, char **argv)
{
	int i;
	int i2;

	if (argc != 3)
	{
		write(1, "\n", 1);
		return (0);
	}
	i = 0;
	i2 = 0;
	while (argv[1][i] != '\0' && argv[2][i2] != '\0')
	{
		if (argv[1][i] == argv[2][i2])
		   i++;
		i2++;
	}
	if (argv[1][i] == '\0')
		write(1, "1\n", 2);
	else write(1, "0\n", 2);
	return (0);
}
