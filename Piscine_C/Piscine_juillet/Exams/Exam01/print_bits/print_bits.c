#include <unistd.h>

void print_bits(unsigned char octet)
{
	char output[8];
	int i;

	i = 8;
	while (--i >= 0)
	{
		output[i] = '0' + octet % 2;
		octet /= 2;
	}
	write(1, output, 8);
}

