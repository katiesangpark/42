#include <unistd.h>

int main(int ac, char **av)
{
	int i;
	int x;

	if (ac == 3)
	{
		i = 0;
		x = 0;
		while (av[2][i])
		{
			if (av[1][x] == av[2][i])
				x++;
			i++;
		}
		if (av[1][x] == 0)
			write(1, av[1], x);
	}
	write(1, "\n", 1);
	return (0);
}
