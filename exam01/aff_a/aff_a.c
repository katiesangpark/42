#include <unistd.h>

int main(int argc, char **argv)
{
	int i;

	if (argc != 2)
		return (0 & write(1, "a\n", 2));
	i = -1;
	while (argv[1][++i] != '\0')
		if (argv[1][i] == 'a')
		{
			write(1, "a\n", 1);
			break ;
		}
	return (0);
}
